/**
 * Custom animation variants factory
 * Allows creating variants with custom duration and delay
 */
export function createAnimationVariants(
  duration: number = 0.5,
  delay: number = 0
): Variants {
  return {
    hidden: {
      opacity: 0,
      y: 20,
    },
    visible: {
      opacity: 1,
      y: 0,
      transition: {
        duration,
        delay,
        ease: "easeOut",
      },
    },
  };
}
